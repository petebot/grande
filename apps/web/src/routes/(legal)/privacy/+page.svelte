  <h1>Privacy</h1>
  <p class="policy-updated">Last updated July 24, 2026</p>

  <p>
    This website is designed to provide Grande Burrito's menu, hours, contact details, directions,
    and public rating information without requiring an account or contact form.
  </p>

  <h2>Information handled automatically</h2>
  <p>
    Like most websites, the hosting provider may receive technical request information such as an IP
    address, browser type, requested page, and request time to deliver and protect the site. This
    site does not intentionally use advertising or analytics cookies.
  </p>

  <h2>Google Maps content</h2>
  <p>
    When configured, the site requests Grande Burrito's public rating and review count from the
    Google Places API through the website server. The request identifies the restaurant, not the
    visitor. If you follow a Google Maps link, Google receives information under the
    <a href="https://policies.google.com/privacy" rel="external">Google Privacy Policy</a>.
  </p>

  <h2>Published restaurant content</h2>
  <p>
    Menu, hours, and restaurant details are delivered from the site's content service or its saved
    continuity snapshot. The public page does not send visitor-entered information to that service.
  </p>

  <h2>Changes</h2>
  <p>
    This notice may be updated when the website's services change. The latest revision date appears
    at the top of this page.
  </p>
